import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

fun Modifier.shimmerBackground(shapeRadius: Dp = 4.dp): Modifier = composed {
    val transition = rememberInfiniteTransition(label = "shimmerTransition")
    val translateAnim = transition.animateFloat(
        initialValue = 0f,
        targetValue = 1000f,
        animationSpec = infiniteRepeatable(
            tween(durationMillis = 1200),
            RepeatMode.Restart
        ), label = "shimmerTranslate"
    )

    val shimmerColors = listOf(
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.6f),
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.2f),
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.6f)
    )

    this.background(
        brush = Brush.linearGradient(
            colors = shimmerColors,
            start = Offset.Zero,
            end = Offset(x = translateAnim.value, y = translateAnim.value)
        ),
        shape = RoundedCornerShape(shapeRadius)
    )
}

@Composable
fun ShimmerPlaceholder(modifier: Modifier = Modifier, height: Dp, shapeRadius: Dp = 8.dp) {
    Spacer(modifier = modifier.height(height).fillMaxWidth().shimmerBackground(shapeRadius))
}